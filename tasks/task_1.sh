#!/bin/bash

echo "1. Список всех файлов в текущей директории с типами:"
file *

echo -e  "\n2. Проверка наличия файла, переданного в качестве аргумента:"
if [ $# -eq 0 ]; then
    echo "Ошибка: не передан аргумент (имя файла для проверки)"
else
    if [ -f "$1" ]; then
        echo "Файл '$1' существует."
    else
        echo "Файл '$1' не найден."
    fi
fi

echo -e "\n3. Информация о каждом файле (имя и права доступа):"
for file in *; do
    echo "File: $file"
    # Выводим права доступа в виде строки (как в ls -l)
    ls -l "$file" | awk '{print "Permissions: "$1}'
    echo "-----"
done
