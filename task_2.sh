#!/bin/bash

# 1. Выводим текущее значение PATH
echo "Текущий PATH: $PATH"

# 2. Добавляем директорию из аргумента в PATH
new_dir="$1"
export PATH="$PATH:$new_dir"

# 3. Выводим обновленный PATH
echo "Обновленный PATH: $PATH"

# 4. Объяснение временности изменений
echo "Изменения временные, так как export действует только в текущей сессии терминала."

# 5. Способ сделать постоянными
echo "Чтобы сделать изменения постоянными, добавьте в файл ~/.bashrc:"
echo "export PATH=\"\$PATH:$new_dir\""
echo "Затем перезапустите терминал или выполните: source ~/.bashrc"
